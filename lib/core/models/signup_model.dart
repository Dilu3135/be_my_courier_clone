class SignupModel {
  final String email;
  final String password;
  final String name;
  final String address;
  final String state;
  final String country;
  final String zip;
  final String city;

  SignupModel({
    required this.email,
    required this.password,
    required this.name,
    required this.address,
    required this.state,
    required this.country,
    required this.zip,
    required this.city,
  });
}
